function createMilestone(input: {
  id: string;
  goalId: string;
  title: string;
  dueDate: string;
  description?: string;
  completed?: boolean;
  completedAt?: string;
}): Milestone {
  return {
    id: input.id,
    goalId: input.goalId,
    title: input.title,
    description: input.description,
    dueDate: input.dueDate,
    completed: input.completed ?? false,
    completedAt: input.completedAt,
  };
}

function createProgress(input: {
  id: string;
  goalId: string;
  value: number;
  date: string;
  note: string;
}): ProgressEntry {
  return {
    id: input.id,
    goalId: input.goalId,
    value: input.value,
    date: input.date,
    note: input.note,
    source: "manual",
  };
}

